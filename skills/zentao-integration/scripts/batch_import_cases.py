def main():
    print(f"🚀 开始批量导入 {len(TEST_CASES)} 个测试用例到ZenTao...")
    print("=" * 60)
    
    success_count = 0
    fail_count = 0
    failed_cases = []
    
    for tc_id, pri, parent, title, steps in TEST_CASES:
        # 构建步骤数组
        formatted_steps = []
        for desc, expect in steps:
            formatted_steps.append({"desc": desc, "expect": expect})
        
        # 构建请求数据
        # 关键：使用parent字段关联需求，使用数组格式steps
        data = {
            "title": f"【{tc_id}】{title}",
            "pri": pri,
            "type": "feature",
            "parent": parent,
            "steps": formatted_steps
        }
        
        # 发送到API
        response = api_post(f"products/{PRODUCT_ID}/testcases", data)
        
        if "id" in response:
            print(f"✓ {tc_id} -> ID:{response['id']}")
            success_count += 1
        else:
            error_msg = str(response.get('error', 'Unknown error'))[:60]
            print(f"✗ {tc_id} -> 失败: {error_msg}")
            fail_count += 1
            failed_cases.append((tc_id, title))
        
        # 避免请求过快
        time.sleep(0.3)
    
    print("=" * 60)
    print(f"📊 导入完成: 成功 {success_count}, 失败 {fail_count}")
    
    if failed_cases:
        print("\n失败的用例:")
        for tc_id, title in failed_cases:
            print(f"  - {tc_id}: {title}")
    
    print("\n✅ 完成!")

if __name__ == "__main__":
    main()
